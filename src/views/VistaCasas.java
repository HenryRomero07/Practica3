    private void guardar() throws IOException{
        if(txtCasa.getText().isEmpty() || txtAncho.getText().isEmpty() || txtLargo.getText().isEmpty() || txtPisos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Datos no ingresados");
        }else
            c.guardarCasa(txtCasa.getText().trim(), Float.parseFloat(txtAncho.getText()), Float.parseFloat(txtLargo.getText()), Integer.parseInt(txtPisos.getText()));
        cargarTabla();
        cargarTablaHomonimos();
        limpiar();
        
    }
    
    private void limpiar(){
        txtCasa.setText("");
        txtAncho.setText("");
        txtLargo.setText("");
        txtPisos.setText("");
    }
    
    public void cargarTabla() throws IOException{
        mt.setMatrizCasas(c.listarCasas());
        jTable1.setModel(mt);
        jTable1.updateUI();
    }
    
    public void cargarTablaHomonimos() throws IOException{
        mth.setDatos(c.obtenerCasasHomonimasParaTabla());
        jTable2.setModel(mth);
        jTable2.updateUI();
    }
    
    

